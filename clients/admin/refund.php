<h2 id='30days'>Refund in First 30 Days</h2>
<p>If you are not completely satisfied with our Web hosting services, and you notify SIMPLE SOFTWARE in writing via your Control Panel within the first 30 days of your contract that you wish to cancel, you will be given a full refund of the contract amount, excluding setup fees and overages. If a refund is due, you will be notified by e-mail; it can take up to 30 days after cancellation of your contract to receive your refund. If you have not received your refund after 30 days, please send an e-mail to <a href='billing@simpsf.com'>billing@simpsf.com</a></p>

<p><strong>Note:</strong> For information about accessing your Control Panel, go to How do I access my Control Panel?Setup fees are refundable ONLY for the following reasons:</p>

<ul>
   <li>You canceled the account order prior to activation</li>
   <li>Your desired domain name was not available.</li>
</ul>

<p>This policy does not apply to any additional services such as, but not limited to, overages, disk space, e-mail accounts, domain name registration, and Web design services.</p>

<h2 id='renewals'>Refund Policy for Renewals</h2>
<p>Charges for the renewal of Web hosting contracts will be refunded if written notification (either via e-mail message or fax transmission) is received no later than the tenth day following the renewal date. This ten day grace period applies to all Web hosting plans. Note that refunds will not be granted if your written notification is received after the grace period.</p>
<p><strong>Warning:</strong> Your e-mail notification to us must be sent from the contact e-mail address that we have on file for your account. Likewise, if you fax your notification to us, your fax must be on letterhead.</p>

<h2 id='domreg'>Domain Registration</h2>

<p>If domain name registration is associated with the account, upon the cancellation of the Web hosting product, the account will be downgraded to Domain Name Registration Only.</p>
<p><strong>Note:</strong> Domain name registration fees are not refundable.For more information about canceling Domain Name Registration Only, go to Non-renew a Domain Name.</p>

<h2 id='toolate'>Requests to Cancel After Renewal Date</h2>
<p>You must notify SIMPLE SOFTWARE on or before ten days of the Renewal Date on your contract if you wish to cancel an account. SIMPLE SOFTWARE does not offer prorated refunds for cancellation requests submitted after ten days of the renewal date.  For more information about canceling your account, go to <a href='/cancel.php' target='_blank'>How do I cancel my account?</a></p>

<h2 id='notransfer'>No Transfer of Funds Between Accounts</h2>
<p>Simple Sofware does not transfer funds from one account to another for any reason.</p>

